function determineOutcome(oppInput, myInput) {
    const oppScore = {
        key: oppInput,
        state: opponentOutcome[oppInput](myInput),
        handScore: choicePoints[opponentKey[oppInput]],
        stateScore: outcomePoints[opponentOutcome[oppInput](myInput)],
        total: choicePoints[opponentKey[oppInput]] + outcomePoints[opponentOutcome[oppInput](myInput)]
    }
    const myScore = {
        key: myInput,
        state: myOutcome[myInput](oppInput),
        handScore: choicePoints[myKey[myInput]],
        stateScore: outcomePoints[myOutcome[myInput](oppInput)],
        total: choicePoints[myKey[myInput]] + outcomePoints[myOutcome[myInput](oppInput)]
    }
    return [oppScore, myScore];
}


